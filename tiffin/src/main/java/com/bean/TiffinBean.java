package com.bean;

public class TiffinBean {
	int tiffinId;
	int restId;
	String tiffinName;
	String tiffinDescription;
	int amount;
	boolean tiffinActive;
	public int getTiffinId() {
		return tiffinId;
	}
	public void setTiffinId(int tiffinId) {
		this.tiffinId = tiffinId;
	}
	public int getRestId() {
		return restId;
	}
	public void setRestId(int restId) {
		this.restId = restId;
	}
	public String getTiffinName() {
		return tiffinName;
	}
	public void setTiffinName(String tiffinName) {
		this.tiffinName = tiffinName;
	}
	public String getTiffinDescription() {
		return tiffinDescription;
	}
	public void setTiffinDescription(String tiffinDescription) {
		this.tiffinDescription = tiffinDescription;
	}
	public int getAmount() {
		return amount;
	}
	public void setAmount(int amount) {
		this.amount = amount;
	}
	public boolean isTiffinActive() {
		return tiffinActive;
	}
	public void setTiffinActive(boolean tiffinActive) {
		this.tiffinActive = tiffinActive;
	}
	
	
}
